using Domain.Commands;
using Services.CommandHandler;
using Services.HelloWorld;

var builder = WebApplication.CreateBuilder(args);


builder.Services.AddControllers();

builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

builder.Services.AddSingleton<ICommandHandler<HelloWorldCmd>, HelloWorldServiceHandler>();
builder.Services.AddSingleton<IHelloWorldService, HelloWorldService>();


var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
